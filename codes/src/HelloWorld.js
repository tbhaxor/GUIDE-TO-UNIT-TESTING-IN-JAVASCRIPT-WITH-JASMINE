/**
 * Method to simple return hello world
 * @returns {String} "Hello World"
 */
function Hello() {
  return "Hello World";
}

/**
 * Method that takes name as input and returns the greeting message with name
 *
 * @param {String} name
 * @returns {String} "Hello, " + name
 */
function HelloWithName(name) {
  return "Hello, " + name;
}
